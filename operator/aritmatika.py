a = 10
b = 5

# penjumlahan (+)
a+b
# pengurangan (-)
a-b
# perkalian (*)
a*b
# pembagian (/)
a/b
# modulo (%)
a % b
# floor division(//)
a//b
# pangkat (**)
a**b

# assignment operator
c = 2
c += 3  # sama aja c ditambah 3. c = c+3
c -= 3  # sama aja c dikurang 3. c = c-3
c *= 3  # sama aja c dikali 3. c = c*3
c /= 3  # sama aja c dibagi 3. c = c/3
c **= 3  # sama aja c dipangkat 3. c = c**3
